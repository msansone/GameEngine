using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace FiremelonEditor2
{
    class Point4D
    {
        public int X;
        public int Y;
        public int Z;
        public int T;

        public Point4D(int X, int Y, int Z, int T)
        {
            this.X = X;
            this.Y = Y;
            this.Z = Z;
            this.T = T;
        }
    }
}
